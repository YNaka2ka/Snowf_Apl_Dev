-- 企業マスターテーブル例
CREATE TABLE company_master (
    company_code VARCHAR(10),
    company_name VARCHAR(100),
    industry VARCHAR(50),
    established_date DATE,
    capital BIGINT,
    employees INTEGER,
    headquarters VARCHAR(100),
    website VARCHAR(200),
    description TEXT
);

-- 売上履歴テーブル例
CREATE TABLE sales_history (
    company_code VARCHAR(10),
    sales_date DATE,
    sales_amount DECIMAL(15,2)
);

